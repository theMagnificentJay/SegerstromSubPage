function btnOnClick1() {
  var i = document.getElementById("button_series_1");
  if (i.innerHTML == "- HIDE PACKAGE CONTENTS") {
    i.innerHTML = "+ SEE PACKAGE CONTENTS";
  } else {
    i.innerHTML = "- HIDE PACKAGE CONTENTS";
  }
}
